int numRookCaptures(vector<vector<char>>& board) {
    int xRook;
    int yRook;

    for(int i = 0; i < 64; ++i) {
        if(board[i / 8][i % 8] == 'R') {
            xRook = i / 8;
            yRook = i % 8;
            break;
        }
    }

    int captures = 0;

    for(int i = xRook; i < 8; ++i) {
        if(board[i][yRook] == 'p') {
            captures++;
            break;
        } else if(board[i][yRook] == 'B') {
            break;
        }
    }

    for(int i = xRook; i > -1; --i) {
        if(board[i][yRook] == 'p') {
            captures++;
            break;
        } else if(board[i][yRook] == 'B') {
            break;
        }
    }

    for(int i = yRook; i < 8; ++i) {
        if(board[xRook][i] == 'p') {
            captures++;
            break;
        } else if(board[xRook][i] == 'B') {
            break;
        }
    }

    for(int i = yRook; i > -1; --i) {
        if(board[xRook][i] == 'p') {
            captures++;
            break;
        } else if(board[xRook][i] == 'B') {
            break;
        }
    }

    return captures;
}

void printBoard(const vector<vector<char>>& board) {
    for (int i = 0; i < 8; ++i) {
        for (int j = 0; j < 8; ++j) {
            cout << board[i][j] << " ";
        }
        cout << endl;
    }
}

void test(vector<vector<char>> board, int expected) {
    cout << "Board:" << endl;
    printBoard(board);

    cout << "Expected: " << expected << endl;

    cout << "Result: " << numRookCaptures(board) << endl;

    cout << endl;
}

